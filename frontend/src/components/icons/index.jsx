export function StatusIdleIcon({ className = '', size = 10 }) {
  return (
    <span
      className={`status-badge status-idle ${className}`}
      style={{ width: size, height: size, minWidth: size, minHeight: size }}
      title="Idle"
    />
  );
}

export function StatusDeactiveIcon({ className = '', size = 10 }) {
  return (
    <span
      className={`status-badge status-deactive ${className}`}
      style={{ width: size, height: size, minWidth: size, minHeight: size }}
      title="Offline"
    />
  );
}

export function getStatusIcon(status, props = {}) {
  const icons = {
    active: StatusActiveIcon,
    idle: StatusIdleIcon,
    deactive: StatusDeactiveIcon,
  };
  const Icon = icons[status] || StatusDeactiveIcon;
  return <Icon {...props} />;
}
